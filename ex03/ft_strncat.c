char	*ft_strncat(char *dest, char *src, unsigned int nb)
{
	int i;
	int j;

	j = 0;
	i = 0;
	while (dest[j])
		j++;
	while (src[i] && i < nb)
	{
		dest[j] = src[i];
		j++;
		i++;
	}
	return (dest);
}
