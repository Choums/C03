char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	while (str[i])
	{
		j = 0;
			while (str[i] == to_find[j])
			{
				j++;
				i++;
				if (to_find[j] == '\0')
				{
					return (str + i - j);
				}
			}
		i++;
	}
	return (0);
}
